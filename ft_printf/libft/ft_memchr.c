#include <stdio.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	letter;
	unsigned char	*str;
	size_t			i;

	str = (unsigned char *)s;
	letter = (unsigned char)c;
	i = 0;
	while (i < n)
	{
		if (*(str + i) == letter)
			return ((void *)str + i);
		i++;
	}
	return (NULL);
}
